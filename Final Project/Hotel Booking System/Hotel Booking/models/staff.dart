class Staff {
  final int id;
  final String name;
  final String role; 
  final String contactInfo;
  final DateTime hireDate;
  final String shift;
  final double salary;
  final String department;

  Staff({
    required this.id,
    required this.name,
    required this.role,
    required this.contactInfo,
    required this.hireDate,
    required this.shift,
    required this.salary,
    required this.department,
  });
}
